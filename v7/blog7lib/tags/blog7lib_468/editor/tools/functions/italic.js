$import("sina/core/class/extend.js");

Core.Class.extend(Editor.Tools.Operate.prototype,{
	    italic: function(){
	        this.editor.history.add();
	        this.editor.execCommand("italic");
			this.editor.swapToolStatus("italic");
	    }
	}
);


